#!/bin/bash

# Проверяем, переданы ли два аргумента
if [ $# -ne 2 ]; then
    echo "Ошибка: нужно указать два числа."
    exit 1
fi

NUM1=$1
NUM2=$2

# Проверяем, являются ли оба аргумента числами
if ! [[ "$NUM1" =~ ^-?[0-9]+$ ]] || ! [[ "$NUM2" =~ ^-?[0-9]+$ ]]; then
    echo "Ошибка: оба аргумента должны быть числами."
    exit 1
fi

# Сравниваем числа и выводим результат
if [ "$NUM1" -gt "$NUM2" ]; then
    echo "$NUM1 больше, чем $NUM2."
elif [ "$NUM1" -lt "$NUM2" ]; then
    echo "$NUM1 меньше, чем $NUM2."
else
    echo "$NUM1 и $NUM2 равны."
fi